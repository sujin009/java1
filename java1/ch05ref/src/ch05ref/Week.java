package ch05ref;

// class , interface , enum 이름을 바꿔서 쓸 수 있다.
// class : 다 구현되어져 있는 코드
// interface : 구현을 아직 안 한 코드 (미완성 코드) - 구성 이름은 같은 이름으로 사용해야하고 구현을 해서 사용을 해야 한다.
// enum : 제한적인 데이터 사용 - ex) 요일 : 7(월~일)가지 데이터를 (나열)열거한다.
public enum Week {
	MONDAY,
	TUESDAY,
	WEDESDAY,
	THURDAY,
	FRIDAY,
	SATURDAY,
	SUNDAY
}
